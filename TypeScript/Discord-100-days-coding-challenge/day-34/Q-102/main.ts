// Question 102: Calculate and log the absolute difference between the number -5 and 5.

let num1: number = -5;
let num2: number = 5;

let absoluteDiff = num2 - num1;
console.log(Math.abs(absoluteDiff));
// This line shows the absolute value of the difference between -5 and 5, ignoring the sign.

/* KEY NOTES:
To calculate the absolute difference between two numbers, you need to ensure that the result is always positive, 
regardless of which number is subtracted from the other. */
