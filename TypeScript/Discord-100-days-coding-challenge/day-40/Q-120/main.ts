// Question 120: Use a for...of loop to iterate through an
//  array of your favorite movies and log each one.

let fave_movies = [
  "Shawshank Redemption",
  "Harry Potter",
  "Saving Private Ryan",
  "Interstellar",
];

for (const movie of fave_movies) {
  console.log(`Favourite movies are: ${movie}`);
}
