// Question 111: Use an if-else-if chain to categorize a person's
//  age group (child, teenager, adult).
var age = 20;
if (age >= 0 && age <= 12) {
    console.log("You are a Child");
}
else if (age >= 13 && age <= 19) {
    console.log("You are a teenager");
}
else {
    console.log("You are an Adult");
}
